class Calculator:
    def __init__(self) -> None:
        pass

    def sum(self, numA:float, numB:float):
        return numA + numB

    def min(self, numA:float, numB:float):
        return numA - numB

    def div(self, numA:float, numB:float):
        return numA / numB

    def mult(self, numA:float, numB:float):
        return numA * numB